""" 1. Explain the difference between map, filter, and reduce. """

""" map:
es una higher build-in funcion que toma una funcion y un iterable como parametros """

""" filter:
es una higher build-in funcion que toma una funcion y un iterable como parametros 
pero la diferencia esta en que retorna un booleano si se cumple la condicion en la iteracion.
En el caso de True retorna ese elemento  """

""" reduce:
es una higher build-in funcion que toma una funcion y un iterable como parametro.
la diferencia esta en que solo retorna un valor """