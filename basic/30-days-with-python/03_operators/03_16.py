## 16. Find the length of the text python and convert the value to float and convert it to string

len_python = len('python')
print('length of python is : ', len_python) # 6
python_to_float = float(len_python)
print('type of python-len : ', python_to_float) # 6.0
float_to_string = str(python_to_float)
print('float value to string : ', type(float_to_string), float_to_string) # <class 'str'> 6.0